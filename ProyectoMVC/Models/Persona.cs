using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ProyectoMVC.Models
{
    class Persona
    {
       

        public int ID
        {

            get
            {
                return ID;
            }

            set
            {
                ID = value;
            }
        }

        public string Nombre
        {

            get
            {
                return Nombre;
            }

            set
            {
                Nombre = value;
            }
        }

        public string Apellido
        {

            get
            {
                return Apellido;
            }

            set
            {
                Apellido = value;
            }
        }

        public string Direccion
        {

            get
            {
                return Direccion;
            }

            set
            {
                Direccion = value;
            }
        }

        public string Ciudad
        {

            get
            {
                return Ciudad;
            }

            set
            {
                Ciudad = value;
            }
        }

        public string Telefono
        {

            get
            {
                return Telefono;
            }

            set
            {
                Telefono = value;
            }
        }

    }
}
